    public int loteria(int a, int b, int c) {
        if ((a == 2) && (b == 2) && (c == 2)) {
            return 10;
        }
        if ((a == b) && (a == c) && (b == c)) {
            return 5;
        }
        if ((a == b) || (a == c) || (b == c)) {
            return 1;
        }

        return 0;
    }

